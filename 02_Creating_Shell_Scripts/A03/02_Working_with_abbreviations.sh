Although nano is a simple editor and quick to learn, vim can offer more. You will
need the full vim-enhanced editor, but we can use abbreviations to common text
we need to type. Once added, we can type_sh in any file we edit, and it will
print as the shebang. Of course, we can have many different abbreviations in our 
.vimrc.